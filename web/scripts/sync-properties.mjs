// Build-time property/unit sync — vendored-with-sync (see docs/CONTENT-MODEL.md §4).
//
// The /properties explorer is `battinfo properties show` for people who have
// not installed anything: every curated spec property and unit with its EMMO
// identity, straight from the canonical mapping tables in
// assets/mappings/domain-battery/**. Committed output keeps the Vercel build
// self-contained; CI fails on drift.
//
//   Run:   npm run sync:properties
//   CI:    npm run sync:properties -- --check

import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(here, "..", "..");
const MAPPINGS = resolve(repoRoot, "assets", "mappings", "domain-battery");
const OUT = resolve(here, "..", "lib", "properties.generated.ts");

const propertyMap = JSON.parse(readFileSync(resolve(MAPPINGS, "property_map.curated.json"), "utf-8"));
const unitMap = JSON.parse(readFileSync(resolve(MAPPINGS, "unit_map.curated.json"), "utf-8"));

const properties = propertyMap.mappings
  .map((m) => ({
    key: m.key,
    label: m.class_pref_label ?? m.class_label ?? m.key,
    qname: m.class_qname ?? "",
    iri: m.class_iri ?? "",
  }))
  .sort((a, b) => a.key.localeCompare(b.key));

const units = unitMap.mappings
  .map((m) => ({
    symbol: m.symbol,
    label: m.unit_pref_label ?? m.unit_label ?? m.symbol,
    qname: m.unit_qname ?? "",
    iri: m.unit_iri ?? "",
  }))
  .sort((a, b) => a.symbol.localeCompare(b.symbol));

const banner = `// GENERATED by scripts/sync-properties.mjs — do not edit.
// Source of truth: assets/mappings/domain-battery/{property_map,unit_map}.curated.json
// (${properties.length} properties, ${units.length} units). CI fails if this file drifts.

`;

const body =
  banner +
  "export const curatedProperties: { key: string; label: string; qname: string; iri: string }[] = " +
  JSON.stringify(properties, null, 2) +
  ";\n\n" +
  "export const curatedUnits: { symbol: string; label: string; qname: string; iri: string }[] = " +
  JSON.stringify(units, null, 2) +
  ";\n";

if (process.argv.includes("--check")) {
  const current = existsSync(OUT) ? readFileSync(OUT, "utf-8") : "";
  if (current.replace(/\r\n/g, "\n") !== body.replace(/\r\n/g, "\n")) {
    console.error(
      "lib/properties.generated.ts drifts from assets/mappings/domain-battery/**.\n" +
        "Run `npm run sync:properties` in web/ and commit the result.",
    );
    process.exit(1);
  }
  console.log(`properties in sync (${properties.length} properties, ${units.length} units).`);
} else {
  writeFileSync(OUT, body);
  console.log(`Wrote ${OUT} (${properties.length} properties, ${units.length} units).`);
}

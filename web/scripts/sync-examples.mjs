// Build-time example sync — vendored-with-sync (see docs/CONTENT-MODEL.md §4).
//
// The website's example fixtures must not drift from the canonical records in
// the repo's examples/** tree. examples.ts is consumed by CLIENT components, so
// the app cannot read examples/** at runtime — instead this script reads the
// canonical record at *author time* and writes a GENERATED, COMMITTED module
// (lib/examples.generated.ts). The committed output keeps the Vercel build
// self-contained (it never reaches outside web/).
//
//   Run:   npm run sync:examples
//   CI:    npm run sync:examples && git diff --exit-code lib/examples.generated.ts
//          (a dirty diff means someone edited the canonical record without
//           re-syncing — fail the build.)
//
// No dependencies; Node ESM only.

import { readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(here, "..", ".."); // web/scripts -> web -> repo root

// Canonical flagship record — the one cell both surfaces feature.
const CANONICAL = resolve(
  repoRoot,
  "examples",
  "cell-spec",
  "A123__ANR26650M1-B.json",
);
const OUT = resolve(here, "..", "lib", "examples.generated.ts");

// Fields a human authors (minted identity fields are assigned at publish, so
// they are dropped from the "authored input" view).
const SPEC_AUTHORED = [
  "name",
  "model",
  "manufacturer",
  "category",
  "cell_format",
  "chemistry",
  "positive_electrode_basis",
];
// A friendly subset of properties for the teaser — the full record carries many
// more; these are the high-signal ones, kept in canonical order.
const PROPERTY_TEASER = [
  "nominal_capacity",
  "nominal_voltage",
  "mass",
  "diameter",
  "height",
];

const pick = (obj, keys) =>
  Object.fromEntries(
    keys.filter((k) => obj && k in obj).map((k) => [k, obj[k]]),
  );

const record = JSON.parse(readFileSync(CANONICAL, "utf-8"));

const cellSpecInput = {
  schema_version: record.schema_version,
  cell_spec: pick(record.cell_spec, SPEC_AUTHORED),
  properties: pick(record.properties, PROPERTY_TEASER),
};

const banner =
  `// @generated by web/scripts/sync-examples.mjs — DO NOT EDIT.\n` +
  `// Source of truth: examples/cell-spec/A123__ANR26650M1-B.json\n` +
  `// Regenerate: npm run sync:examples   (see docs/CONTENT-MODEL.md §4)\n\n`;

const body =
  `// The full canonical record, verbatim from examples/**.\n` +
  `export const cellSpecCanonical = ${JSON.stringify(record, null, 2)} as const;\n\n` +
  `// A friendly authored-input teaser derived from the canonical record, so its\n` +
  `// values can never drift from the source record.\n` +
  `export const cellSpecInput = ${JSON.stringify(cellSpecInput, null, 2)} as const;\n`;

writeFileSync(OUT, banner + body);
// eslint-disable-next-line no-console
console.log(
  `synced ${OUT.replace(repoRoot, ".")} from ${CANONICAL.replace(repoRoot, ".")}`,
);
